-- Creating a database for the E-commerce project

create database Ecommerce_Sales;

-- Using the created database

use Ecommerce_Sales;

-- Creating customers table to store customer information

create table customers(
CustomerID int primary key,
FirstName varchar(80),
LastName varchar(60),
Email varchar(100),
City varchar(80),
State varchar(80),
Country varchar(50),
JoinDate date);

-- Creating products table to store products information

create table products(
ProductID int primary key,
ProductName varchar(100),
Category varchar(50),
Price decimal(10,3),
Cost decimal(10,3));

-- Creating orders  table to store orders information

create table orders (
OrderID int primary key,
CustomerID int,
OrderDate date,
TotalAmount decimal(10,3),
foreign key (CustomerID) references customers(CustomerID)
);

-- Creating order_details  table to store order_details information

create table order_details (
OrderDetailID int,
OrderID int,
ProductID int,
Quantity int,
Discount decimal(5,3),
foreign key (OrderID) references orders(OrderID),
foreign key (ProductID) references products(ProductID)
);

